public class LaStructureIterativeFor {
    public static void main(String... args) {
        // for have tree parts cut by semicolon
        // the first part on the right it's declaration and affectation
        // the second part is the stop condition
        // the third part is increment or decrement it depends on the condition
        for (int i = 0; i < 3; i++){
            System.out.println("Hello World");
        }
    }
}
