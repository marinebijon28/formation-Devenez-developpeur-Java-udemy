public class LesOperateursRelationnels {
    public static void main(String... args)
    {
        // declaration and affectation
        // superior
        boolean bool = 20 > 30;
        System.out.println(bool);
        // inferior
        bool = 20 < 30;
        System.out.println(bool);
        // equivalent
        bool = 20 == 30;
        System.out.println(bool);
        // different
        bool = 20 != 30;
        System.out.println(bool);
        // superior or equal
        bool = 20 >=30;
        System.out.println(bool);
        // inferior or equal
        bool = 20 <= 30;
        System.out.println(bool);

        // declaration and affectation
        int nb1 = 20;
        int nb2 = 30;
        bool = nb1 < nb2;
        System.out.println(bool);

        // declaration and affectation
        char c1 = 'c';
        char c2 = 'y';
        bool = c1 < c2;
        System.out.println(bool);

        // declaration and affectation
        boolean bool1 = true;
        boolean bool2 = false;
        bool = c1 == c2;
        System.out.println(bool);
    }
}
